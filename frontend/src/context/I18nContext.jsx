const languageAliases = {
  english: "en",
  en: "en",
  hindi: "hi",
  hi: "hi",
  marathi: "mr",
  mr: "mr",
};

const localeMap = {
  en: "en-IN",
  hi: "hi-IN",
  mr: "mr-IN",
};

const I18nContext = createContext(null);

function getValueByPath(source, path) {
  return path.split(".").reduce((current, key) => current?.[key], source);
}

function interpolate(template, values) {
  return Object.entries(values || {}).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value ?? "")),
    template,
  );
}

export function resolveLanguageCode(language) {
  return languageAliases[String(language || "English").trim().toLowerCase()] || "en";
}

export function I18nProvider({ children }) {
  const { user } = useAuth();

  const value = useMemo(() => {
    const code = resolveLanguageCode(user?.language);
    const locale = localeMap[code] || localeMap.en;

    const t = (path, values) => {
      const currentValue =
        getValueByPath(translations[code], path) ?? getValueByPath(translations.en, path) ?? path;
      return typeof currentValue === "string" ? interpolate(currentValue, values) : currentValue;
    };

    return {
      language: code,
      locale,
      t,
      formatDate: (value, options) =>
        new Intl.DateTimeFormat(locale, options || {
          day: "numeric",
          month: "short",
          year: "numeric",
        }).format(new Date(value)),
      formatDateTime: (value, options) =>
        new Intl.DateTimeFormat(locale, options || {
          day: "numeric",
          month: "short",
          year: "numeric",
          hour: "2-digit",
          minute: "2-digit",
        }).format(new Date(value)),
    };
  }, [user?.language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }

  return context;
}
